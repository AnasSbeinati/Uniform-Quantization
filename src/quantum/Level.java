package quantum;

public class Level {
	private String code;
	private int RQ;
	private int min;
	private int max;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public int getRQ() {
		return RQ;
	}
	public void setRQ(int rQ) {
		RQ = rQ;
	}
	public int getMin() {
		return min;
	}
	public void setMin(int min) {
		this.min = min;
	}
	public int getMax() {
		return max;
	}
	public void setMax(int max) {
		this.max = max;
	}
}
